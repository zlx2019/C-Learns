/**
 * I/O 即输入输出(Input/Output)，对于任何编程语言，IO都是非常核心的功能；
 * 程序运行需要数据，那么数据从何而来？
 *  数据的获取往往需要通过外部媒介进行通信，这个媒介可能是本地文件、数据库、网络IO等等。
 * 简单来说，能够进行输入(Input)与输出(Output)的都可以称之为IO设备，统一称为IO流;
 *
 * IO流是一种顺序读写数据的模式，它的特点是单向流动。数据类似自来水一样在水管中流动，所以我们把它称为IO流。
 * IO流以byte（字节）为最小单位，因此也称为字节流，在C语言中，一个char就等于一个byte;
 */
